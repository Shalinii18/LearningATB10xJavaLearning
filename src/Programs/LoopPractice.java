package Programs;

public class LoopPractice {

    // TELL EVEN ODD FROM RANGE 1 TO 50

    public static void main(String[] args) {

        for (int a = 1; a <= 50 ; a++)
        {
            if (a%2==0)
            System.out.println("Even is =" +a);
            else{
            System.out.println("Odd");}
        }

    }
}
